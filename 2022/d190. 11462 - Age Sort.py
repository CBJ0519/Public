'''
(url: https://zerojudge.tw/ShowProblem?problemid=d190)

內容
給你某國家所有一歲以上 (含) 的人民的年齡。你知道該國沒有人活到 100 歲或更老。
現在給你一個很簡單的工作，就是把所有的年齡由小到大排序。

輸入說明
輸入檔有若干組測試資料。每組測資以整數 n (0<n<=2000000) 開始，代表人數。
下一行則有 n 個整數，代表他們的年齡。
n = 0 代表輸入的結束，請勿處理這組測資。

輸出說明
對於每組測資，印出一行以空白隔開的 n 個整數。這些整數為人民的年齡，由小到大排列。

範例輸入 #1
5
3 4 2 1 5
5
2 3 2 3 1
0
範例輸出 #1
1 2 3 4 5
1 2 2 3 3
'''
while True:
    if input()=='0': break
    s=[int(x) for x in input().split()]
    print(*sorted(s),sep=' ')
