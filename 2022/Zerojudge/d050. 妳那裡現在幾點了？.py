'''
(url: https://zerojudge.tw/ShowProblem?problemid=d050)

內容
珊珊到了美國猶他州的楊百翰大學之後，文文禁不住對她的思念，常常想打電話給她，卻又擔心在美國的她是不是在睡覺。
好不容易鼓起勇氣打通了電話，第一句就先問：「妳那裡現在幾點了？」請你幫文文寫一個程式，輸入台灣時間後，算出美國山區時間。

輸入說明
輸入僅有一行，其中含有一個整數 h (0 ≤ h ≤ 23)，代表台灣時間現在幾點 (24 小時制)。
因為美國時間的分與台灣時間的分相同，因此幾分就不用輸入計算了。

輸出說明
輸出美國山區時間 (24 小時制)。台灣的時間比美國山區時間快 15 小時 (不考慮日光節約時間)。

範例輸入 #1
21
範例輸出 #1
6
範例輸入 #2
11
範例輸出 #2
20
'''
a=int(input())
if a>=15: print(a-15)
else: print(24+a-15)
